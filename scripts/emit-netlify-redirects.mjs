/**
 * Writes public/_redirects before Vite copies it to dist/.
 * Netlify cannot substitute env vars into netlify.toml redirects; the /api proxy
 * must use the same origin as VITE_API_ORIGIN baked into the bundle.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const dest = path.join(__dirname, '..', 'public', '_redirects')

const raw = String(process.env.VITE_API_ORIGIN || '').trim().replace(/\/$/, '')
const lines = ['# Generated by scripts/emit-netlify-redirects.mjs — do not edit by hand.']

if (raw && /^https:\/\//i.test(raw)) {
  try {
    const u = new URL(raw)
    if (u.hostname.includes('.')) {
      lines.push(`/api/*  ${u.origin}/api/:splat  200`)
    }
  } catch {
    /* skip proxy line */
  }
}

if (lines.length === 1) {
  console.warn(
    '[emit-netlify-redirects] No valid VITE_API_ORIGIN — Netlify /api proxy omitted. Set VITE_API_ORIGIN to your Render service root (https://….onrender.com) in Netlify → Environment variables → Build.',
  )
}

lines.push('/*    /index.html   200')
fs.writeFileSync(dest, `${lines.join('\n')}\n`)
console.log('[emit-netlify-redirects] wrote', dest)
